/**
 * Get TopStrike username for a wallet address
 * Returns null if no mapping exists
 */
export function getTopStrikeUsername(walletAddress: string): string | null {
  const normalizedAddress = walletAddress.toLowerCase()
  return TOPSTRIKE_USERNAMES[normalizedAddress] || null
}

/**
 * Get wallet address for a TopStrike username
 * Returns null if no mapping exists
 */
export function getWalletByUsername(username: string): string | null {
  const normalizedUsername = username.toLowerCase().trim()

  for (const [wallet, mappedUsername] of Object.entries(TOPSTRIKE_USERNAMES)) {
    if (mappedUsername.toLowerCase() === normalizedUsername) {
      return wallet
    }
  }

  return null
}
